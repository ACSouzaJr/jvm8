class iOperations {
  public static int main(String[] s) {
    int a = 5, b = 4, c;
    c = a - b;
    System.out.println(c);
    c = a * b;
    System.out.println(c);
    c = a / b;
    System.out.println(c);
    c = a % b;
    System.out.println(c);
    c = a + b;
    System.out.println(c);
    return c;
  }
}
