class fOperations {
  public static float main(String[] s) {
    float a = 5.5f, b = 4.6f, c;
    c = a - b;
    System.out.println(c);
    c = a * b;
    System.out.println(c);
    c = a / b;
    System.out.println(c);
    c = a % b;
    System.out.println(c);
    c = a + b;
    System.out.println(c);
    return c;
  }
}
